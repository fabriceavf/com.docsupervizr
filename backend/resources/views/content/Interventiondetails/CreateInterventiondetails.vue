<template>
    <b-overlay :show="isLoading">
        <form @submit.prevent="createLine()">
            <div class="mb-3">


                <div class="form-group">
                    <label>jour </label>
                    <input v-model="form.jour" :class="errors.jour?'form-control is-invalid':'form-control'"
                           type="text">

                    <div v-if="errors.jour" class="invalid-feedback">
                        <template v-for=" error in errors.jour"> {{ error[0] }}</template>

                    </div>
                </div>


                <div class="form-group">
                    <label>debut </label>
                    <input v-model="form.debut" :class="errors.debut?'form-control is-invalid':'form-control'"
                           type="text">

                    <div v-if="errors.debut" class="invalid-feedback">
                        <template v-for=" error in errors.debut"> {{ error[0] }}</template>

                    </div>
                </div>


                <div class="form-group">
                    <label>fin </label>
                    <input v-model="form.fin" :class="errors.fin?'form-control is-invalid':'form-control'"
                           type="text">

                    <div v-if="errors.fin" class="invalid-feedback">
                        <template v-for=" error in errors.fin"> {{ error[0] }}</template>

                    </div>
                </div>


                <div class="form-group">
                    <label>interventionusers </label>
                    <CustomSelect
                        :key="form.interventionuser"
                        :columnDefs="['libelle']"
                        :oldValue="form.interventionuser"
                        :renderCallBack="(data)=>`${data.Selectlabel}`"
                        :selectCallBack="(data)=>form.interventionuser_id=data.id"
                        :url="`${axios.defaults.baseURL}/api/interventionusers-Aggrid`"
                        filter-key=""
                        filter-value=""
                    />
                    <div v-if="errors.interventionuser_id" class="invalid-feedback">
                        <template v-for=" error in errors.interventionuser_id"> {{ error[0] }}</template>

                    </div>
                </div>

            </div>

            <button class="btn btn-primary" type="submit">
                <i class="fas fa-floppy-disk"></i> Cr√©er
            </button>
        </form>
    </b-overlay>
</template>

<script>
import CustomSelect from "@/components/CustomSelect.vue";

import Files from "@/components/Files.vue"
import VSelect from 'vue-select'

export default {
    name: 'CreateInterventiondetails',
    components: {VSelect, CustomSelect, Files},
    props: [
        'gridApi',
        'modalFormId',
        'interventionusersData',
    ],
    data() {
        return {
            errors: [],
            isLoading: false,
            form: {

                id: "",

                interventionuser_id: "",

                jour: "",

                debut: "",

                fin: "",

                created_at: "",

                updated_at: "",
            }
        }
    },
    methods: {
        createLine() {
            this.isLoading = true
            this.axios.post('/api/interventiondetails', this.form).then(response => {
                this.isLoading = false
                this.resetForm()
                this.gridApi.applyServerSideTransaction({
                    add: [
                        response.data
                    ],
                });
                this.gridApi.refreshServerSide()
                this.$bvModal.hide(this.modalFormId)
                this.$toast.success('Operation effectuer avec succes')
                this.$emit('close')
                console.log(response.data)
            }).catch(error => {
                this.errors = error.response.data.errors
                this.isLoading = false
                this.$toast.error('Erreur survenue lors de l\'enregistrement')
            })
        },
        resetForm() {
            this.form = {
                id: "",
                interventionuser_id: "",
                jour: "",
                debut: "",
                fin: "",
                created_at: "",
                updated_at: "",
            }
        }
    }
}
</script>
